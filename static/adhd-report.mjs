import { NON_DIAGNOSTIC_STATEMENTS } from "./adhd-data.mjs";
import { getReportContext } from "./adhd-scoring.mjs";

export function buildMarkdownReport(responses, result) {
  const context = getReportContext(responses, result);

  return `# Adult ADHD Screening & Visit Preparation Summary

This is a self-report summary generated by Vincent ADHD. It is not a diagnosis, treatment plan, medication recommendation, or substitute for professional evaluation.

## 1. Reason for using Vincent

${context.reasonForUsingVincent}

## 2. Screening signal

${context.signal}

${NON_DIAGNOSTIC_STATEMENTS.map((statement) => `- ${statement}`).join("\n")}

## 3. Current attention/executive-function patterns

${context.currentPatterns.attention}

## 4. Restlessness/impulsivity patterns

${context.currentPatterns.restlessness}

## 5. Functional impairment examples

${context.functionalImpact}

${responses.recentExamples?.trim() ? `Additional recent examples: ${responses.recentExamples.trim()}` : ""}

## 6. Childhood-history evidence

${context.childhoodHistory}

## 7. Cross-setting pattern

${context.crossSettingPattern}

## 8. Other factors to discuss with a clinician

${context.overlapFactors.map((factor) => `- ${factor}`).join("\n")}

${responses.otherFactorsNotes?.trim() ? `Additional notes: ${responses.otherFactorsNotes.trim()}` : ""}

## 9. Questions the user may want to ask

${context.clinicianQuestions}

## 10. Safety note

${context.safetyNote}

## 11. Privacy note

Generated locally in browser. Answers are processed in the browser and are not sent to Vincent servers.`;
}
